package org.example.javaFundamentals.oktober.dvadsiatydruhy;

public class Boolean2 {
    public static void main(String[] args) {
        System.out.println(3 > 2);
        int a = 8;
        int b = 2;
        System.out.println(a < b);
        System.out.println(a == b); //rovná sa
        System.out.println(a != b); //nerovná sa

        // command + option + L ---> upraví mi to kód aby bol dobre čitateľný
        //v kombinácii s option sa mi napíšu znaky, ktoré mám na klávesnici v pravo dole
    }
}
